package stringEx;

public class CharAtEx {

	public static void main(String[] args) {
		
		String name = "NileshRathod";
		
		char ch = name.charAt(9); //Returns the char value at 4th index.

		char ch1 = name.charAt(7);
		
		char ch2 = name.charAt(8);
		
		System.out.print(ch);
	
		System.out.print(ch1);
		
		System.out.print(ch2);
		
	}

}
