package stringEx;

public class Substring {

	public static void main(String[] args) {
		
		String s1  = "NileshRathod";
		
		System.out.println(s1.substring(0,6)); //returns Nilesh
		
		System.out.println(s1.substring(6));  //Returns Rathod
		//The string starts from the index no. we assigned to the substring
	}

}
